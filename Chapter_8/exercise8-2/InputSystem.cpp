void InputSystem::SetActionFile(std::string FileName)
{
    std::ifstream in{FileName};
    std::string line;
    
    while(getline(in,line))
    {
        std::string tmp;
        std::istringstream stream(line);
        std::unordered_map<int,std::string> data;
        
        for(int i=0;getline(stream,tmp,',');i++)
        {
            data[i] = tmp;
        }
        //ボタン配列とデバイス配列にアクションと結びつけている
        ButtonAllay[data[0]] = data[2];
        DeviceAllay[data[0]] = data[1];
    }
    
    //std::cout<<ButtonAllay["Fire"]<<std::endl;
    //std::cout<<DeviceAllay["Fire"]<<std::endl;
}

ButtonState InputState::GetMappedButtonState(const std::string& actionName) const
{
    ButtonState mstate;
    mstate = EEllor;
    
    InputSystem tmp;
    tmp.SetActionFile("action.txt");
    //デバイスがコントロラーだったら
    if(tmp.DeviceAllay[actionName] == "Controller")
    {
        if(tmp.ButtonAllay[actionName] == "A")
        {
            //std::cout<<"state:"<<tmp.ButtonAllay[actionName]<<std::endl;
            mstate = Controller.GetButtonState(SDL_CONTROLLER_BUTTON_A);
        }
        else  mstate = EEllor;
    }
    else mstate = EEllor;
    
    
    //std::cout<<"state:"<<tmp.ButtonAllay[actionName]<<std::endl;
    return mstate;
}
